      "ðŸ˜‡"
  """
  def binary_to_emoji(binary) when is_binary(binary) do
    hash = :crypto.hash(:sha256, binary)
    index = :binary.decode_unsigned(hash) |> rem(length(@emojis))
    Enum.at(@emojis, index)
  end
